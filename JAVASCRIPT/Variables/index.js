        // Using 'var': Global or function-scoped
        var greeting = "Hello, World!";
        console.log("Using var: " + greeting); 

        // Reassigning a var variable
        greeting = "Hello, JavaScript!";
        console.log("Updated var: " + greeting); 

        // Using 'let': Block-scoped
        let age = 25;
        console.log("Using let: My age is " + age); 

        // Reassigning a let variable
        age = 30;
        console.log("Updated let: My new age is " + age); 

        // Using 'const': Block-scoped and constant
        const pi = 3.14;
        console.log("Using const: The value of Pi is " + pi); 


        //  Combining variables
        let firstName = "Zhahi Tech";
        let lastName = "Training";
        console.log("Full Name: " + firstName + " " + lastName); 
       
        //Using variables in a sentence
        let favoriteColor = "blue";
        console.log("My favorite color is " + favoriteColor);
